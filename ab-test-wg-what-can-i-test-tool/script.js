function generateTable() {
  let device = document.querySelector('input[name="device"]:checked').value;
  let placement = document.getElementById("placementType").value;
  let placementData;

  // Determine which data to use based on the device and placement type
  switch (`${device} ${placement}`) {
    case "Desktop ASM":
      placementData = DesktopASM;
      break;
    case "Desktop BPT":
      placementData = DesktopBPT;
      break;
    case "Desktop LNK":
      placementData = DesktopLNK;
      break;
    case "Mobile Browser ASM":
      placementData = MobileBrowserASM;
      break;
    case "Mobile Browser BPT":
      placementData = MobileBrowserBPT;
      break;
    case "Mobile Browser LNK":
      placementData = null;
      break;
    case "App ASM":
      placementData = MobileAppASM;
      break;
    case "App BPT":
      placementData = MobileAppBPT;
      break;
    case "App LNK":
      placementData = MobileAppLNK;
      break;
    default:
      placementData = null;
  }

  let tableArea = document.getElementById("tableArea");
  tableArea.innerHTML = ""; // Clear previous content

  // Check if placementData is defined
  if (placementData) {
    // Generate the table with the placement data
    for (let element in placementData) {
      let section = document.createElement("div");
      section.innerHTML = `<h3>${element}</h3>`;
      let table = document.createElement("table");
      table.className = "table-full-width";
      //   table.style.border = "1px solid black";
      //   table.style.borderCollapse = "collapse";

      let attribute = placementData[element];
      let row = table.insertRow();
      let cell1 = row.insertCell(0);
      let cell2 = row.insertCell(1);

      cell1.innerHTML = attribute["Description"];
      cell2.innerHTML = attribute["image of Buttons"];
      cell2.className = "image-cell"; // Apply image cell class to cell2

      section.appendChild(table);
      tableArea.appendChild(section);
    }
  } else {
    tableArea.innerHTML = "<p>No data available for the selected options.</p>";
  }
}
